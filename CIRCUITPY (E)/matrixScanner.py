# Lets ponder the different ways that a switch can be implemented:

# A switch can be directly connected to a GPIO Pin
# A switch can be directly connected to a Multiplexer
# A switch can be connected as a member in a Matrix
# A switch can be connected as a member in a Shift Register

# At its core, a Switch Matrix is a collection of inputs and outputs
# When the inputs are in a specific electrical configuration
# The outputs can be read to gain information about which switches are pressed. 

# Each Matrix input and output can also have variety of implementations:
# An input/output can be directly connected to a GPIO Pin
# An input/output can be directly connected to a Multiplexer

